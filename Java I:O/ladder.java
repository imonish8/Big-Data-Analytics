public class ladder{
		public static void main(String[] args){
			char fun = 'A';
			char fun2 = 'N';
			char fun3 = 'A';
			
			if(fun == fun2){
			System.out.println("Fun is not equal to Fun 2");
			}
			else if(fun3 == fun2){
			System.out.println("Fun 3 is not equal to Fun 2");
			}
			else{
			System.out.println("Reached, fun1 and fun3 have A value");
			System.out.println("Fun 2 has N");
			System.out.println("\n");
			}	
	}
}
