public class static_values {
		static int default_int;
		static float default_flt;
		static char default_chr;
		static String default_str;
		static long default_long;
		static byte default_byte;
		static short default_shrt;
		
		public static void main(String[] args){
		
		System.out.println("Default value for Integer : "+default_int);
		System.out.println("Default value for Float : "+default_flt);
		System.out.println("Default value for Character : "+default_chr);
		System.out.println("Default value for String : "+default_str);
		System.out.println("Default value for Long : "+default_long);
		System.out.println("Default value for Byte : "+default_byte);
		System.out.println("Default value for Short : "+default_shrt);

	
		
		}
}
