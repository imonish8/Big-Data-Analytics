public class instance_values{
			public static void main(String[] args){
			int a;
			Short s;
			long l;
			byte b;
			String str;
			double d;
			float f;
			char ch;
			
			
			
		System.out.println("Default value of Integer : "+a);
		System.out.println("Default value of Short : "+s);
		System.out.println("Default value of Long : "+l);
		System.out.println("Default value of Byte : "+b);
		System.out.println("Default value of string : "+str);
		System.out.println("Default value of Double is : "+d);
		System.out.println("Default value of Float is : "+f);
		System.out.println("Default value of Character is : "+ch);
	}
}
